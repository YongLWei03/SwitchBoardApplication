package com.board.applicion.mode.cable;

public class CableBean {

    private int attr;
    private String cableNum;
    private String cableSpec;
    private String cableTypeName;
    private long edCabinetId;
    private String ending;
    private long id;
    private String qrCode;
    private long stCabinetId;
    private String starting;
    private String substationName;
    private String mcrName;

    public int getAttr() {
        return attr;
    }

    public void setAttr(int attr) {
        this.attr = attr;
    }

    public String getCableNum() {
        return cableNum;
    }

    public void setCableNum(String cableNum) {
        this.cableNum = cableNum;
    }

    public String getCableSpec() {
        return cableSpec;
    }

    public void setCableSpec(String cableSpec) {
        this.cableSpec = cableSpec;
    }

    public String getCableTypeName() {
        return cableTypeName;
    }

    public void setCableTypeName(String cableTypeName) {
        this.cableTypeName = cableTypeName;
    }

    public long getEdCabinetId() {
        return edCabinetId;
    }

    public void setEdCabinetId(long edCabinetId) {
        this.edCabinetId = edCabinetId;
    }

    public String getEnding() {
        return ending;
    }

    public void setEnding(String ending) {
        this.ending = ending;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getQrCode() {
        return qrCode;
    }

    public void setQrCode(String qrCode) {
        this.qrCode = qrCode;
    }

    public long getStCabinetId() {
        return stCabinetId;
    }

    public void setStCabinetId(long stCabinetId) {
        this.stCabinetId = stCabinetId;
    }

    public String getStarting() {
        return starting;
    }

    public void setStarting(String starting) {
        this.starting = starting;
    }

    public String getSubstationName() {
        return substationName;
    }

    public void setSubstationName(String substationName) {
        this.substationName = substationName;
    }

    public String getMcrName() {
        return mcrName;
    }

    public void setMcrName(String mcrName) {
        this.mcrName = mcrName;
    }
}
